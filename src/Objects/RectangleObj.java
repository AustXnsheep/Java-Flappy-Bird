package Objects;

public class RectangleObj {
    public int x, y, width, height;
    public RectangleObj(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
    public void setX(int x) {
        this.x = x;
    }
}
